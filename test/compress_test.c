	pindf_uchar_str_init(dest, 1024);
	pindf_uchar_str_init(decompressed, 1024);


	memcpy(dest->p, Engrampaper, sizeof(Engrampaper));
	dest->len = sizeof(Engrampaper);

	ret = pindf_flate_decode(&flate_filter, decompressed, dest);

	if (ret < 0) {
		printf("[error] decompress error\n");
		return -1;
	}

	FILE *fp = fopen("decompressed.out", "wb");
	fwrite(decompressed->p, sizeof(uchar), decompressed->len, fp);

